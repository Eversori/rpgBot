package rpgClasses;


public class Stat
{
	int value; // value of the stat

	String label; // name of the stat


	public int getValue()
	{
		return value;
	}

	public void setValue(int value)
	{
		this.value = value;
	}

	public String getLabel()
	{
		return label;
	}

	public void setLabel(String label)
	{
		this.label = label;
	}
}
